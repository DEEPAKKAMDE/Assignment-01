package javapackage;

public class IfElseLadderDemo {

	public static void main(String[] args) {

		int p = 300; // Local Variable
		int q = 300; // Local Variable

		if (p < q ) {
			System.out.println("P is Lesser");
		} else if (p == q) {
			System.out.println(" P is equal to Q");
		}

		else {

			System.out.println("P is Greater");
		}

	}

}
