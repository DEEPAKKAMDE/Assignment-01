package javaassignment01;

public class ForEachDemo {

	public static void main(String[] args) {


		// Define an integer array
		int a[] = { 10, 20, 30, 60, 0, 100, 2000, 400, 99, 33 };

		// Print an Array using for each loop

		for(int b: a) {

			System.out.println(b);
		}


	}

}
