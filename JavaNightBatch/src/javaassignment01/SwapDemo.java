package javaassignment01;

public class SwapDemo {

	public static void main(String[] args) {

		int a=10;
		int b =20;
		int temp;

		temp=a; // temp=10
		a=b;// a=20
		b=temp; // b=10

		System.out.println("Value of a is = "+a);
		System.out.println("Value of b is = "+b);
	}

}
