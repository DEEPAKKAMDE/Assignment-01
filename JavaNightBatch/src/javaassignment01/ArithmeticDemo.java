package javaassignment01;

public class ArithmeticDemo {

	public static void main(String[] args) {

		int a = 10;
		int b = 20;

		int sum = a + b;
		int sub = b - a;
		int mult = a * b;
		int div = b / a;
		int mod = b % a;
		System.out.println(sum);
		System.out.println(sub);
		System.out.println(mult);
		System.out.println(div);
		System.out.println(mod);

	}

}
