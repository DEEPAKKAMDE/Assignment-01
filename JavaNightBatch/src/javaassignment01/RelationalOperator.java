package javaassignment01;

public class RelationalOperator {

	public static void main(String[] args) {

		int a = 100; // Local Variable
		int b = 200; // Local Variable

		System.out.println(a > b);// false

		System.out.println(a < b);// true

		System.out.println(a >= b);// false

		System.out.println(a <= b);// true

		System.out.println(a == b);// false

		System.out.println(a != b);// true

	}


}
